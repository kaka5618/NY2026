-- =============================================================================
-- 3. 聊天会话表 chat_sessions（每用户 × 每角色一条主会话）
-- =============================================================================
CREATE TABLE chat_sessions (
  id               bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id          bigint       NOT NULL REFERENCES users (id) ON DELETE CASCADE,
  character_id     smallint     NOT NULL REFERENCES characters (id) ON DELETE RESTRICT,
  title            varchar(64),
  last_message_at  timestamptz,
  created_at       timestamptz  NOT NULL DEFAULT now(),
  updated_at       timestamptz,
  CONSTRAINT uq_chat_sessions_user_character UNIQUE (user_id, character_id)
);

CREATE INDEX idx_chat_sessions_user ON chat_sessions (user_id);
CREATE INDEX idx_chat_sessions_last_msg ON chat_sessions (user_id, last_message_at DESC NULLS LAST);

COMMENT ON TABLE chat_sessions IS '用户与某一角色的会话容器；每人每角色默认一条';
COMMENT ON COLUMN chat_sessions.id IS '会话主键序号，自增';
COMMENT ON COLUMN chat_sessions.user_id IS '所属用户';
COMMENT ON COLUMN chat_sessions.character_id IS '对话角色，关联 characters.id';
COMMENT ON COLUMN chat_sessions.title IS '会话标题，可空';
COMMENT ON COLUMN chat_sessions.last_message_at IS '最后一条消息时间，用于列表排序';
COMMENT ON COLUMN chat_sessions.created_at IS '会话创建时间';
COMMENT ON COLUMN chat_sessions.updated_at IS '会话最后更新时间';

-- =============================================================================
-- 4. 聊天消息表 chat_messages
-- =============================================================================
CREATE TABLE chat_messages (
  id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  session_id  bigint       NOT NULL REFERENCES chat_sessions (id) ON DELETE CASCADE,
  role        varchar(16)  NOT NULL,
  content     text         NOT NULL,
  reply_type  varchar(32),
  extra_json  jsonb,
  created_at  timestamptz  NOT NULL DEFAULT now(),
  CONSTRAINT ck_chat_messages_role CHECK (role IN ('user', 'assistant'))
);

CREATE INDEX idx_chat_messages_session_time ON chat_messages (session_id, created_at);

COMMENT ON TABLE chat_messages IS '会话内的单条消息（用户或助手）';
COMMENT ON COLUMN chat_messages.id IS '消息主键序号，自增';
COMMENT ON COLUMN chat_messages.session_id IS '所属会话';
COMMENT ON COLUMN chat_messages.role IS '发送方：user 用户，assistant 助手';
COMMENT ON COLUMN chat_messages.content IS '消息正文（文本）';
COMMENT ON COLUMN chat_messages.reply_type IS '多模态类型：text / voice / image 等，与接口约定一致';
COMMENT ON COLUMN chat_messages.extra_json IS '扩展字段：语音 URL、图片、image_ref 等 JSON';
COMMENT ON COLUMN chat_messages.created_at IS '消息产生时间';
