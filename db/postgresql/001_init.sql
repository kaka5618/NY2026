-- 虚拟男友项目 — PostgreSQL 初始库表
-- 执行示例：psql "$DATABASE_URL" -v ON_ERROR_STOP=1 -f db/postgresql/001_init.sql

-- =============================================================================
-- 1. 用户表 users
-- =============================================================================
CREATE TABLE users (
  id           bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username     varchar(32)  NOT NULL,
  password_hash varchar(255) NOT NULL,
  email        varchar(255) NOT NULL,
  nickname     varchar(32)  NOT NULL,
  created_at   timestamptz  NOT NULL DEFAULT now(),
  updated_at   timestamptz,
  status       smallint     NOT NULL DEFAULT 1,
  CONSTRAINT ck_users_status CHECK (status IN (0, 1))
);

-- 用户名、邮箱唯一（不区分大小写）
CREATE UNIQUE INDEX uq_users_username_lower ON users (lower(username));
CREATE UNIQUE INDEX uq_users_email_lower ON users (lower(email));

CREATE INDEX idx_users_created_at ON users (created_at);

COMMENT ON TABLE users IS '注册用户：登录名、密码哈希、邮箱、昵称与账号状态';
COMMENT ON COLUMN users.id IS '用户主键序号，自增';
COMMENT ON COLUMN users.username IS '登录用户名；唯一性由 lower(username) 唯一索引保证';
COMMENT ON COLUMN users.password_hash IS '密码哈希（如 bcrypt），禁止存明文';
COMMENT ON COLUMN users.email IS '注册邮箱；唯一性由 lower(email) 唯一索引保证';
COMMENT ON COLUMN users.nickname IS '展示昵称';
COMMENT ON COLUMN users.created_at IS '注册时间';
COMMENT ON COLUMN users.updated_at IS '资料或安全相关字段最后更新时间';
COMMENT ON COLUMN users.status IS '账号状态：1 正常，0 禁用';

-- =============================================================================
-- 2. 角色字典表 characters（与业务 character id 对齐，便于外键）
-- =============================================================================
CREATE TABLE characters (
  id          smallserial PRIMARY KEY,
  code        varchar(32)  NOT NULL,
  name        varchar(32)  NOT NULL,
  sort_order  smallint     NOT NULL DEFAULT 0,
  is_active   boolean      NOT NULL DEFAULT true,
  created_at  timestamptz  NOT NULL DEFAULT now(),
  CONSTRAINT uq_characters_code UNIQUE (code)
);

COMMENT ON TABLE characters IS '可对话角色字典，供会话表外键引用';
COMMENT ON COLUMN characters.id IS '角色主键序号，自增';
COMMENT ON COLUMN characters.code IS '角色代码，如 shenyu、lushiyan，与程序内 CharacterId 一致';
COMMENT ON COLUMN characters.name IS '角色展示名称';
COMMENT ON COLUMN characters.sort_order IS '首页或列表排序权重，越小越靠前';
COMMENT ON COLUMN characters.is_active IS '是否对用户可见';
COMMENT ON COLUMN characters.created_at IS '记录创建时间';

